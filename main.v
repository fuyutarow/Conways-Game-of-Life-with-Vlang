fn print_field(field[]array_int) {
	for line in field {
		mut s := '    '
		for j, cell in line {
			if j == 0 || j == line.len - 1{continue}
			s += if cell == 1{'@'} else { '.'}
		}
		println(s)
	}
	println('')
}

